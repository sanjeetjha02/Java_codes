public class print_pattern {
    public static void main(String[] args) {
        //practice prooblem 1 ch 5
        int n=10;
        for(int i=n; i>0;i--){
            for(int j=0;j<i;j++){
                System.out.print("*");

            }
            System.out.print("\n");
        }
        
    }
    
}